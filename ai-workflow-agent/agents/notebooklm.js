/**
 * NotebookLM-style content analysis.
 * Uses Gemini SDK if GEMINI_API_KEY present, otherwise OpenRouter.
 * @param {string} content  — text/document to analyse
 * @param {'analyze'|'notes'|'quiz'|'podcast'|'mindmap'} [mode]
 */
async function askNotebookLM(content, mode) {
  mode = mode || 'analyze';
  const prefix = MODE_PROMPTS[mode] || '';
  const prompt = prefix + content;

  // Gemini — same tech as the real NotebookLM
  if (process.env.GEMINI_API_KEY) {
    try {
      const { GoogleGenerativeAI } = require('@google/generative-ai');
      const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
      const model = genAI.getGenerativeModel({
        model: 'gemini-1.5-pro',
        systemInstruction: SYSTEM_PROMPT,
      });
      const result = await model.generateContent(prompt);
      return result.response.text();
    } catch (err) {
      console.warn('[NotebookLM] Gemini \u0641\u0634\u0644\u061b \u0627\u0644\u062a\u062d\u0648\u064a\u0644 \u0625\u0644\u0649 OpenRouter:', err.message);
    }
  }

  // Fallback — best available free model for long-context understanding
  const { askOpenRouter } = require('./openrouter');
  return askOpenRouter(
    prompt,
    'qwen/qwen3.6-plus-preview:free',
    SYSTEM_PROMPT,
    { max_tokens: 4000 }
  );
}

